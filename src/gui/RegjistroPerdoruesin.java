    private void tabelaSelectedIndexChange() {
        final ListSelectionModel rowSM = tabela.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent Ise) {
                if (Ise.getValueIsAdjusting()) {
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) Ise.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    Perdoruesit p = ptm.getPerdoruesi(selectedIndex);
                    emri.setText(p.getEmriPerdoruesit());
                    fjalekalimi.setText(p.getFjalekalimi());
                    perserit.setText(p.getFjalekalimi());
                    niveliCB.setSelectedItem(p.getNiveliPerdorimit());

                }
            }
        });
    }

