    private void savePerdoruesitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savePerdoruesitButtonActionPerformed
        // TODO add your handling code here:
        if (!emri.getText().trim().equals("") && !fjalekalimi.getText().trim().equals("") && !perserit.getText().trim().equals("") && niveliCB.getSelectedItem() != null) {
            int row = tabela.getSelectedRow();
            if(row == -1){
            Perdoruesit p = new Perdoruesit();
            try {
                p.setEmriPerdoruesit(emri.getText());
            } catch (FarmaciaException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }

            if (!fjalekalimi.getText().equals(perserit.getText())) {
                JOptionPane.showMessageDialog(this, "Fjalekalimet ndryshojne.Ju lutem provoni perseri.");
                return;
            }
            try {
                p.setFjalekalimi(fjalekalimi.getText());
                p.setNiveliPerdorimit((String) niveliCB.getSelectedItem());
                try {
                    pr.create(p);
                } catch (FarmaciaException fe) {
                    JOptionPane.showMessageDialog(this, fe.getMessage());
                }
            } catch (FarmaciaException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
            }else {
                Perdoruesit p = ptm.getPerdoruesi(row);
                try {
                p.setEmriPerdoruesit(emri.getText());
            } catch (FarmaciaException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }

            if (!fjalekalimi.getText().equals(perserit.getText())) {
                JOptionPane.showMessageDialog(this, "Fjalekalimet ndryshojne.Ju lutem provoni perseri.");
                return;
            }
            try {
                p.setFjalekalimi(fjalekalimi.getText());
                p.setNiveliPerdorimit((String) niveliCB.getSelectedItem());
                pr.edit(p);
            } catch (FarmaciaException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
                
            }
            loadTable();
            clear();
        } else {
            JOptionPane.showMessageDialog(this, "Ju lutem plotesoni fushat obligative");
        }



    }//GEN-LAST:event_savePerdoruesitButtonActionPerformed

