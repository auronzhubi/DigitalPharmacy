    public void loadCombobox(){
        try {
            List<Komuna> list = kir.findAll();
            kcm = new KomunaComboboxModel(list);
            komunaCB.setModel(kcm);
            komunaCB.repaint();

        } catch (FarmaciaException se) {
            JOptionPane.showMessageDialog(this, se.getMessage());

        }
    }

    public void loadTable() {
        List<Furnizuesi> list = fr.findAll();
        ftm = new FurnizuesiTableModel(list);
        tabela.setModel(ftm);
        ftm.fireTableDataChanged();

    }

    private void tabelaSelectedIndexChange() {
        final ListSelectionModel rowSM = tabela.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent Ise) {
                if (Ise.getValueIsAdjusting()) {
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) Ise.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    Furnizuesi f = ftm.getFurnizuesi(selectedIndex);
                    
                    emri.setText(f.getEmriFurnizuesit() + "");
                    komunaCB.setSelectedItem(f.getKomunaID());
                    komunaCB.repaint();
                    numriTel.setText(f.getNumriTelefonit());
                }
            }
        });
    }

