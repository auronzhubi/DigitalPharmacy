    private void saveFurnizuesiButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveFurnizuesiButtonActionPerformed
        // TODO add your handling code here:
        if (!emri.getText().trim().equals("")) {
            int row = tabela.getSelectedRow();
           if (row == -1) {
                Furnizuesi f = new Furnizuesi();
                try{
                f.setEmriFurnizuesit(emri.getText());
                Komuna k = (Komuna) komunaCB.getSelectedItem();
                f.setKomunaID(k);
                //f.setKomuna((String) komuna.getSelectedItem());
                f.setNumriTelefonit(numriTel.getText());
                try {
                    fr.create(f);
                } catch (FarmaciaException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
                }catch(FarmaciaException ex){
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
            } else {
                Furnizuesi f = ftm.getFurnizuesi(row);
                try{
                f.setEmriFurnizuesit(emri.getText());
                Komuna k = (Komuna) komunaCB.getSelectedItem();
                f.setKomunaID(k);
                f.setNumriTelefonit(numriTel.getText());
                fr.edit(f);
                }catch(FarmaciaException ex){
                    JOptionPane.showMessageDialog(this, ex.getMessage());                    
                }
                
                spf.furnizuesiCB.repaint();
           }
            clear();
            loadTable();
           
        } else {
            JOptionPane.showMessageDialog(this, "Ju lutem plotesoni fushat obligative");
        }
    }//GEN-LAST:event_saveFurnizuesiButtonActionPerformed

